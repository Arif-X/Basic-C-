#include<iostream>
using namespace std;

int main(){
	//	 Integer
	int myInt = 10;
	cout << "My Integer : " << myInt << "\n";
	
	//	 Double
	double myDouble = 10.0;
	cout << "My Double : " << myDouble << "\n";
	
	//	 Char
	char myChar = 'A';
	cout << "My Char : " << myChar << "\n";
	
	//	 String
	string myString = "Aku Ganteng";
	cout << "My String : " << myString << "\n";
	
	//	 Boolean
	bool myBoolean = true;
	cout << "My Boolean : " << myBoolean << "\n";
}
